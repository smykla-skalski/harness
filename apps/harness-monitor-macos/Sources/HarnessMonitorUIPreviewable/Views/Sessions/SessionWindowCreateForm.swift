import HarnessMonitorKit
import SwiftUI

struct SessionWindowCreateForm: View {
  let store: HarnessMonitorStore
  let state: SessionWindowStateCache
  let draft: SessionCreateDraft
  let embedsRuntimeConfiguration: Bool
  @Environment(\.fontScale)
  var fontScale
  @State private var stateStorage = SessionWindowCreateFormStateStorage()
  @FocusState var focusedFieldStorage: SessionWindowCreateFormField?

  var validationResult: SessionWindowCreateFormValidationResult? {
    get { stateStorage.validationResult }
    nonmutating set { stateStorage.validationResult = newValue }
  }

  var lastAutoGeneratedName: String {
    get { stateStorage.lastAutoGeneratedName }
    nonmutating set { stateStorage.lastAutoGeneratedName = newValue }
  }

  var openRouterModels: [OpenRouterModelEntry] {
    get { stateStorage.openRouterModels }
    nonmutating set { stateStorage.openRouterModels = newValue }
  }

  var openRouterUsageSnapshot: OpenRouterModelUsageSnapshot {
    get { stateStorage.openRouterUsageSnapshot }
    nonmutating set { stateStorage.openRouterUsageSnapshot = newValue }
  }

  var isLoadingOpenRouterModels: Bool {
    get { stateStorage.isLoadingOpenRouterModels }
    nonmutating set { stateStorage.isLoadingOpenRouterModels = newValue }
  }

  var isPresentingOpenRouterBrowser: Bool {
    get { stateStorage.isPresentingOpenRouterBrowser }
    nonmutating set { stateStorage.isPresentingOpenRouterBrowser = newValue }
  }

  var openRouterModelsLoadedFor: String? {
    get { stateStorage.openRouterModelsLoadedFor }
    nonmutating set { stateStorage.openRouterModelsLoadedFor = newValue }
  }

  var focusedField: SessionWindowCreateFormField? {
    get { focusedFieldStorage }
    nonmutating set { focusedFieldStorage = newValue }
  }

  var focusedFieldBinding: FocusState<SessionWindowCreateFormField?>.Binding {
    $focusedFieldStorage
  }

  var metrics: SessionWindowCreateFormMetrics {
    SessionWindowCreateFormMetrics(fontScale: fontScale)
  }

  var body: some View {
    Form {
      if draft.kind == .agent, embedsRuntimeConfiguration {
        embeddedAgentRuntimeSections
      }
      Section {
        LabeledContent("Name") {
          TextField("", text: title)
            .harnessNativeTextField()
            .focused(focusedFieldBinding, equals: .name)
            .accessibilityLabel("\(draft.kind.title) name")
            .accessibilityHint(validationMessage(for: .name) ?? "")
        }
        LabeledContent("Prompt") {
          HarnessMonitorMultilineTextField(
            placeholder: "",
            text: prompt,
            minHeight: metrics.promptMinHeight,
            focusedField: focusedFieldBinding,
            equals: .prompt,
            accessibilityLabel: "Prompt"
          )
        }
      } header: {
        HStack(spacing: HarnessMonitorTheme.spacingXS) {
          Text(draft.kind.title)
            .harnessNativeFormSectionHeader()
          Spacer(minLength: 0)

          if draft.kind == .agent {
            Button {
              regenerateAgentName()
            } label: {
              Image(systemName: "arrow.clockwise")
            }
            .harnessActionButtonStyle(variant: .bordered, tint: .secondary)
            .controlSize(HarnessMonitorControlMetrics.compactControlSize)
            .help("Generate a new agent name")
            .accessibilityLabel("Regenerate agent name")
          }
        }
        .frame(maxWidth: .infinity, alignment: .leading)
      }
      if draft.kind == .agent, embedsRuntimeConfiguration {
        SessionWindowCreateBridgeBannerSection(
          store: store,
          selection: normalizedLaunchSelection
        )
      }
      if draft.kind == .agent {
        agentConfigurationSections
      }
      if draft.kind == .task {
        Section {
          Picker("Severity", selection: taskSeverity) {
            ForEach(TaskSeverity.allCases, id: \.rawValue) { severity in
              Text(severity.title).tag(severity)
            }
          }
          .accessibilityLabel("Task severity")
        } header: {
          Text("Task Details")
            .harnessNativeFormSectionHeader()
        }
      }
      if let validationResult {
        Section {
          Text(validationResult.message)
            .scaledFont(.callout)
            .foregroundStyle(.red)
            .accessibilityLabel("Validation error: \(validationResult.message)")
        }
      }
      Section {
        HStack {
          Button("Cancel", role: .cancel) {
            cancel()
          }
          Spacer()
          Button {
            Task { await submit() }
          } label: {
            Label("Create", systemImage: "plus.circle.fill")
              .scaledFont(.body.weight(.semibold))
          }
          .frame(minHeight: metrics.submitButtonMinHeight)
          .keyboardShortcut(.defaultAction)
        }
      }
    }
    .harnessNativeFormContainer()
    .contentMargins(.horizontal, metrics.formPadding, for: .scrollContent)
    .contentMargins(.vertical, metrics.formPadding, for: .scrollContent)
    .scrollContentBackground(.hidden)
    .dynamicTypeSize(.xSmall ... .accessibility5)
    .task {
      await SessionGeometryWritebackDeferral.nextMainActorTurn()
      prefillAgentNameIfEligible()
      if draft.kind == .agent, embedsRuntimeConfiguration {
        await SessionWindowCreateFormCatalogs.loadAgentCatalogStateIfNeeded(
          store: store,
          state: state,
          draft: draft
        )
      }
      guard focusedField == nil else { return }
      await SessionGeometryWritebackDeferral.nextMainActorTurn()
      if focusedField == nil {
        focusedField = .name
      }
    }
    .onChange(of: draft.runtime) { _, _ in
      prefillAgentNameIfEligible()
      clearValidationIfResolved()
    }
    .onChange(of: draft.prompt) { _, _ in
      clearValidationIfResolved()
    }
    .task(id: selectedAcpDescriptorID) {
      await loadOpenRouterModelsIfNeeded()
    }
    .sheet(isPresented: $stateStorage.isPresentingOpenRouterBrowser) {
      OpenRouterModelBrowserSheet(
        models: openRouterModels,
        usage: store.openRouterModelUsage,
        usageSnapshot: $stateStorage.openRouterUsageSnapshot,
        onSelect: { modelID in
          if let runtimeKey = selectedModelCatalogRuntimeKey {
            updateRuntimeModelPickerSelection(modelID, for: runtimeKey)
          }
        }
      )
    }
  }

  func loadOpenRouterModelsIfNeeded() async {
    guard isOpenRouterAcpSelected else { return }
    if openRouterModelsLoadedFor == HarnessMonitorStore.openRouterDescriptorID,
      !openRouterModels.isEmpty
    {
      openRouterUsageSnapshot = store.openRouterModelUsage.snapshot()
      return
    }
    isLoadingOpenRouterModels = true
    defer { isLoadingOpenRouterModels = false }
    openRouterUsageSnapshot = store.openRouterModelUsage.snapshot()
    let models = await store.fetchOpenRouterModels()
    openRouterModels = models
    openRouterModelsLoadedFor = HarnessMonitorStore.openRouterDescriptorID
  }
}

enum SessionWindowCreateFormField: Hashable {
  case name
  case prompt
  case commandOverride
}
