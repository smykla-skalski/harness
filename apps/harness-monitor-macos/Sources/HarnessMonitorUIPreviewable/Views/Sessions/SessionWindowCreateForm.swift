import HarnessMonitorKit
import SwiftUI

struct SessionWindowCreateFormMetrics: Equatable {
  let formPadding: CGFloat
  let promptMinHeight: CGFloat
  let submitButtonMinHeight: CGFloat

  init(fontScale: CGFloat) {
    let scale = SessionWindowFontScale.metricsScale(for: fontScale)
    formPadding = 24 * min(scale, 1.3)
    promptMinHeight = max(90, 90 * min(scale, 1.25))
    submitButtonMinHeight = scale >= 1.45 ? 44 : 0
  }
}

private struct SessionWindowCreateFormStateStorage {
  var validationResult: SessionWindowCreateFormValidationResult?
  var lastAutoGeneratedName = ""
}

struct SessionWindowCreateForm: View {
  let store: HarnessMonitorStore
  let state: SessionWindowStateCache
  let draft: SessionCreateDraft
  let embedsRuntimeConfiguration: Bool
  @Environment(\.fontScale)
  private var fontScale
  @State private var stateStorage = SessionWindowCreateFormStateStorage()
  @FocusState private var focusedFieldStorage: SessionWindowCreateFormField?

  private var title: Binding<String> {
    Binding(
      get: { draft.title },
      set: { updateDraft(title: $0) }
    )
  }

  private var prompt: Binding<String> {
    Binding(
      get: { draft.prompt },
      set: { updateDraft(prompt: $0) }
    )
  }

  private var taskSeverity: Binding<TaskSeverity> {
    Binding(
      get: { draft.taskSeverity },
      set: { updateDraft(taskSeverity: $0) }
    )
  }

  var validationResult: SessionWindowCreateFormValidationResult? {
    get { stateStorage.validationResult }
    nonmutating set { stateStorage.validationResult = newValue }
  }

  var lastAutoGeneratedName: String {
    get { stateStorage.lastAutoGeneratedName }
    nonmutating set { stateStorage.lastAutoGeneratedName = newValue }
  }

  var focusedField: SessionWindowCreateFormField? {
    get { focusedFieldStorage }
    nonmutating set { focusedFieldStorage = newValue }
  }

  var focusedFieldBinding: FocusState<SessionWindowCreateFormField?>.Binding {
    $focusedFieldStorage
  }

  var activeAgentCapabilityOptions: [AgentCapabilityOption] {
    SessionWindowCreateFormCatalogs.activeAgentOptions(
      catalogState: state.agentCreateCatalog,
      store: store
    )
  }

  private var metrics: SessionWindowCreateFormMetrics {
    SessionWindowCreateFormMetrics(fontScale: fontScale)
  }

  var body: some View {
    Form {
      if draft.kind == .agent, embedsRuntimeConfiguration {
        Section {
          SessionWindowCreateAgentRuntimeContent(
            store: store,
            state: state,
            draft: draft,
            embeddedInForm: true
          )
        }
      }
      Section {
        HStack(spacing: 6) {
          TextField("Name", text: title)
            .scaledFont(.body)
            .focused(focusedFieldBinding, equals: .name)
            .accessibilityLabel("\(draft.kind.title) name")
            .accessibilityHint(validationMessage(for: .name) ?? "")
          if draft.kind == .agent {
            Button {
              regenerateAgentName()
            } label: {
              Image(systemName: "arrow.clockwise")
            }
            .buttonStyle(.borderless)
            .help("Generate a new agent name")
            .accessibilityLabel("Regenerate agent name")
          }
        }
        TextEditor(text: prompt)
          .scaledFont(.body)
          .frame(minHeight: metrics.promptMinHeight)
          .focused(focusedFieldBinding, equals: .prompt)
          .accessibilityLabel("Prompt")
        if draft.kind == .agent {
          agentConfigurationFields
        }
      } header: {
        Text(draft.kind.title)
          .harnessNativeFormSectionHeader()
      }
      if draft.kind == .task {
        Section {
          Picker("Severity", selection: taskSeverity) {
            ForEach(TaskSeverity.allCases, id: \.rawValue) { severity in
              Text(severity.title).tag(severity)
            }
          }
          .accessibilityLabel("Task severity")
        } header: {
          Text("Task Details")
            .harnessNativeFormSectionHeader()
        }
      }
      if let validationResult {
        Section {
          Text(validationResult.message)
            .scaledFont(.callout)
            .foregroundStyle(.red)
            .accessibilityLabel("Validation error: \(validationResult.message)")
        }
      }
      Section {
        HStack {
          Button("Cancel", role: .cancel) {
            cancel()
          }
          Spacer()
          Button {
            Task { await submit() }
          } label: {
            Label("Create", systemImage: "plus.circle.fill")
              .scaledFont(.body.weight(.semibold))
          }
          .frame(minHeight: metrics.submitButtonMinHeight)
          .keyboardShortcut(.defaultAction)
        }
      }
    }
    .harnessNativeFormContainer()
    .padding(metrics.formPadding)
    .dynamicTypeSize(.xSmall ... .accessibility5)
    .task {
      if focusedField == nil {
        focusedField = .name
      }
      prefillAgentNameIfEligible()
      await SessionWindowCreateFormCatalogs.loadAgentCatalogStateIfNeeded(
        store: store,
        state: state,
        draft: draft
      )
    }
    .onChange(of: draft.runtime) { _, _ in
      prefillAgentNameIfEligible()
      clearValidationIfResolved()
    }
    .onChange(of: draft.useCodex) { _, _ in
      prefillAgentNameIfEligible()
      clearValidationIfResolved()
    }
    .onChange(of: draft.codexModel) { _, _ in
      clearValidationIfResolved()
    }
    .onChange(of: draft.codexEffort) { _, _ in
      clearValidationIfResolved()
    }
    .onChange(of: draft.prompt) { _, _ in
      clearValidationIfResolved()
    }
  }

  func updateDraft(
    title: String? = nil,
    prompt: String? = nil,
    runtime: String? = nil,
    role: SessionRole? = nil,
    fallbackRole: SessionRole? = nil,
    personaID: String? = nil,
    projectDir: String? = nil,
    argvOverride: String? = nil,
    taskSeverity: TaskSeverity? = nil,
    useCodex: Bool? = nil,
    codexMode: CodexRunMode? = nil,
    codexModel: String? = nil,
    codexEffort: String? = nil,
    codexAllowCustomModel: Bool? = nil
  ) {
    var next = draft
    if let title {
      next.title = title
    }
    if let prompt {
      next.prompt = prompt
    }
    if let runtime {
      next.runtime = runtime
    }
    if let role {
      next.role = role
    }
    if let fallbackRole {
      next.fallbackRole = fallbackRole
    }
    if let personaID {
      next.personaID = personaID
    }
    if let projectDir {
      next.projectDir = projectDir
    }
    if let argvOverride {
      next.argvOverride = argvOverride
    }
    if let taskSeverity {
      next.taskSeverity = taskSeverity
    }
    if let useCodex {
      next.useCodex = useCodex
    }
    if let codexMode {
      next.codexMode = codexMode
    }
    if let codexModel {
      next.codexModel = codexModel
    }
    if let codexEffort {
      next.codexEffort = codexEffort
    }
    if let codexAllowCustomModel {
      next.codexAllowCustomModel = codexAllowCustomModel
    }
    clearValidationIfNeeded(title: title, runtime: runtime, useCodex: useCodex)
    state.updateCreateDraft(next)
  }

  private func cancel() {
    validationResult = nil
    state.cancelCreateDraft(draft.kind)
  }
}

enum SessionWindowCreateFormField: Hashable {
  case name
  case prompt
}

extension SessionWindowCreateForm {
  private var catalogState: SessionWindowAgentCreateCatalogState {
    state.agentCreateCatalog
  }

  private var activePersonas: [AgentPersona] {
    catalogState.personas
  }

  private var normalizedLaunchSelection: AgentLaunchSelection {
    SessionWindowCreateFormCatalogs.normalizedLaunchSelection(
      draft: draft,
      options: activeAgentCapabilityOptions
    )
  }

  private var selectedCapabilityOption: AgentCapabilityOption? {
    SessionWindowCreateFormCatalogs.selectedCapabilityOption(
      selection: normalizedLaunchSelection,
      options: activeAgentCapabilityOptions
    )
  }

  private var selectedTransportChoice: AgentCapabilityTransportChoice? {
    selectedCapabilityOption.map { option in
      option.transportChoice(for: normalizedLaunchSelection)
    }
  }

  private var selectedTerminalRuntime: AgentTuiRuntime? {
    guard case .tui(let runtime) = normalizedLaunchSelection else { return nil }
    return runtime
  }

  private var selectedTerminalCatalog: RuntimeModelCatalog? {
    guard let selectedTerminalRuntime else { return nil }
    return SessionWindowCreateFormCatalogs.selectedModelCatalog(
      selection: .tui(selectedTerminalRuntime),
      catalogState: catalogState
    )
  }

  private var codexCatalog: RuntimeModelCatalog? {
    SessionWindowCreateFormCatalogs.codexModelCatalog(catalogState: catalogState)
  }

  private var showsAcpFallbackRoleMenu: Bool {
    SessionWindowCreateFormCatalogs.shouldShowAcpFallbackRole(
      selection: normalizedLaunchSelection,
      role: draft.role
    )
  }

  private var launchSelection: Binding<AgentLaunchSelection> {
    Binding(
      get: { normalizedLaunchSelection },
      set: { updateDraft(runtime: $0.storageKey) }
    )
  }

  private var selectedRole: Binding<SessionRole> {
    Binding(
      get: { draft.role },
      set: { updateDraft(role: $0) }
    )
  }

  private var selectedFallbackRole: Binding<SessionRole> {
    Binding(
      get: { draft.fallbackRole },
      set: { updateDraft(fallbackRole: $0) }
    )
  }

  private var useCodex: Binding<Bool> {
    Binding(
      get: { draft.useCodex },
      set: { updateDraft(useCodex: $0) }
    )
  }

  private var selectedPersonaID: Binding<String> {
    Binding(
      get: { draft.personaID },
      set: { updateDraft(personaID: $0) }
    )
  }

  private var projectDirOverride: Binding<String> {
    Binding(
      get: { draft.projectDir },
      set: { updateDraft(projectDir: $0) }
    )
  }

  private var argvOverrideText: Binding<String> {
    Binding(
      get: { draft.argvOverride },
      set: { updateDraft(argvOverride: $0) }
    )
  }

  private var codexMode: Binding<CodexRunMode> {
    Binding(
      get: { draft.codexMode },
      set: { updateDraft(codexMode: $0) }
    )
  }

  private var codexModelPickerSelection: Binding<String> {
    Binding(
      get: {
        if draft.codexAllowCustomModel {
          return SessionWindowCreateFormCatalogs.RuntimeCustomModel.tag
        }
        let stored = draft.codexModel.trimmingCharacters(in: .whitespacesAndNewlines)
        if !stored.isEmpty {
          return stored
        }
        return codexCatalog?.default ?? SessionWindowCreateFormCatalogs.RuntimeCustomModel.tag
      },
      set: { newValue in
        if newValue == SessionWindowCreateFormCatalogs.RuntimeCustomModel.tag {
          updateDraft(codexAllowCustomModel: true)
        } else {
          updateDraft(
            codexModel: newValue,
            codexAllowCustomModel: false
          )
        }
      }
    )
  }

  private var codexCustomModel: Binding<String> {
    Binding(
      get: { draft.codexAllowCustomModel ? draft.codexModel : "" },
      set: {
        updateDraft(
          codexModel: $0,
          codexAllowCustomModel: true
        )
      }
    )
  }

  private var codexEffortValues: [String] {
    guard let codexCatalog else {
      return codexModelPickerSelection.wrappedValue
        == SessionWindowCreateFormCatalogs.RuntimeCustomModel.tag
        ? SessionWindowCreateFormCatalogs.allEffortLevels
        : []
    }
    return SessionWindowCreateFormCatalogs.effortValues(
      catalog: codexCatalog,
      selectedModelID: codexModelPickerSelection.wrappedValue
    )
  }

  private var codexEffortSelection: Binding<String> {
    Binding(
      get: {
        let current = draft.codexEffort.trimmingCharacters(in: .whitespacesAndNewlines)
        guard codexEffortValues.contains(current) else {
          return SessionWindowCreateFormCatalogs.defaultEffortLevel(from: codexEffortValues)
        }
        return current
      },
      set: { updateDraft(codexEffort: $0) }
    )
  }

  @ViewBuilder
  private var agentConfigurationFields: some View {
    if draft.useCodex {
      codexConfigurationFields
    } else {
      terminalConfigurationFields
    }
  }

  @ViewBuilder
  private var terminalConfigurationFields: some View {
    terminalAdvancedOverrides

    if let option = selectedCapabilityOption, let choice = selectedTransportChoice {
      terminalRuntimeConfigurationFields
      roleMenu
      if showsAcpFallbackRoleMenu {
        acpFallbackRoleMenu
      }
      personaMenu
      terminalTransportNotice(option: option, choice: choice)
    } else {
      SessionWindowCreateFieldBlock(title: "Provider") {
        Text(
          embedsRuntimeConfiguration
            ? "Choose a provider in the middle pane to configure this agent."
            : "Choose a provider above to configure this agent."
        )
          .scaledFont(.caption)
          .foregroundStyle(HarnessMonitorTheme.secondaryInk)
          .fixedSize(horizontal: false, vertical: true)
      }
      roleMenu
      personaMenu
    }
  }

  private var terminalAdvancedOverrides: some View {
    DisclosureGroup("Advanced overrides") {
      VStack(alignment: .leading, spacing: HarnessMonitorTheme.sectionSpacing) {
        SessionWindowCreateFieldBlock(
          title: "Project directory override"
        ) {
          TextField("Optional project directory override", text: projectDirOverride)
            .harnessNativeTextField()
            .accessibilityLabel("Project directory override")
        }

        if !normalizedLaunchSelection.isAcp {
          SessionWindowCreateFieldBlock(
            title: "Command override",
            help: "One argument per line. The first line is the executable."
          ) {
            TextEditor(text: argvOverrideText)
              .scaledFont(.body)
              .frame(minHeight: 100)
              .accessibilityLabel("Command override")
          }
        }
      }
      .padding(.top, HarnessMonitorTheme.spacingSM)
    }
    .accessibilityLabel("Advanced overrides")
  }

  @ViewBuilder
  private func terminalTransportChoicesSection(option: AgentCapabilityOption) -> some View {
    if option.transportChoices.count > 1 {
      SessionWindowCreateFieldBlock(
        title: "Start with",
        help: "Choose whether this provider opens in Terminal or joins with project access."
      ) {
        ViewThatFits(in: .horizontal) {
          HStack(alignment: .top, spacing: HarnessMonitorTheme.spacingSM) {
            ForEach(option.transportChoices) { transportChoice in
              SessionWindowCreateTransportChoiceButton(
                providerTitle: option.title,
                choice: transportChoice,
                selection: launchSelection,
                isSelected: normalizedLaunchSelection == transportChoice.id,
                isEnabled: option.isEnabled(transportChoice),
                unavailableReason: SessionWindowCreateFormCatalogs.unavailableReason(
                  for: option,
                  choice: transportChoice
                )
              )
            }
          }

          VStack(alignment: .leading, spacing: HarnessMonitorTheme.spacingSM) {
            ForEach(option.transportChoices) { transportChoice in
              SessionWindowCreateTransportChoiceButton(
                providerTitle: option.title,
                choice: transportChoice,
                selection: launchSelection,
                isSelected: normalizedLaunchSelection == transportChoice.id,
                isEnabled: option.isEnabled(transportChoice),
                unavailableReason: SessionWindowCreateFormCatalogs.unavailableReason(
                  for: option,
                  choice: transportChoice
                )
              )
            }
          }
        }
      }
    }
  }

  @ViewBuilder
  private var terminalRuntimeConfigurationFields: some View {
    if let runtime = selectedTerminalRuntime, let catalog = selectedTerminalCatalog {
      SessionWindowCreateFieldBlock(title: "Model") {
        Picker(
          selectedTerminalModelMenuTitle(runtime: runtime, catalog: catalog),
          selection: terminalModelPickerSelection(for: runtime, catalog: catalog)
        ) {
          ForEach(catalog.models) { model in
            Text(model.displayName).tag(model.id)
          }
          Text("Custom...")
            .tag(SessionWindowCreateFormCatalogs.RuntimeCustomModel.tag)
        }
        .pickerStyle(.menu)
        .harnessNativeFormControl()
        .accessibilityLabel("Runtime model")

        if terminalModelPickerSelection(for: runtime, catalog: catalog).wrappedValue
          == SessionWindowCreateFormCatalogs.RuntimeCustomModel.tag
        {
          TextField(
            "Provider-specific model id",
            text: terminalCustomModel(for: runtime)
          )
          .harnessNativeTextField()
          .accessibilityLabel("Custom runtime model")
        }
      }

      let effortValues = terminalEffortValues(for: runtime, catalog: catalog)
      if !effortValues.isEmpty {
        SessionWindowCreateFieldBlock(title: "Effort") {
          Picker("Effort", selection: terminalEffortSelection(for: runtime, values: effortValues)) {
            ForEach(effortValues, id: \.self) { level in
              Text(level.capitalized).tag(level)
            }
          }
          .pickerStyle(.segmented)
          .harnessNativeFormControl()
          .accessibilityLabel("Runtime effort")
        }
      }
    } else if normalizedLaunchSelection.isAcp {
      SessionWindowCreateFieldBlock(title: "Project access") {
        Text("Project access uses the provider's configured defaults.")
          .scaledFont(.caption)
          .foregroundStyle(HarnessMonitorTheme.secondaryInk)
          .fixedSize(horizontal: false, vertical: true)
      }
    } else {
      SessionWindowCreateFieldBlock(title: "Runtime defaults") {
        Text("This runtime does not publish additional model controls here.")
          .scaledFont(.caption)
          .foregroundStyle(HarnessMonitorTheme.secondaryInk)
          .fixedSize(horizontal: false, vertical: true)
      }
    }
  }

  private var roleMenu: some View {
    SessionWindowCreateFieldBlock(title: "Role in session") {
      Picker("Role", selection: selectedRole) {
        ForEach(SessionRole.allCases, id: \.self) { role in
          Text(role.title).tag(role)
        }
      }
      .pickerStyle(.menu)
      .harnessNativeFormControl()
      .accessibilityLabel("Role")
    }
  }

  private var personaMenu: some View {
    SessionWindowCreateFieldBlock(title: "Persona (optional)") {
      Picker("Persona", selection: selectedPersonaID) {
        Text("None").tag("")
        ForEach(activePersonas, id: \.identifier) { persona in
          Text(persona.name).tag(persona.identifier)
        }
      }
      .pickerStyle(.menu)
      .harnessNativeFormControl()
      .accessibilityLabel("Persona")

      Text(
        SessionWindowCreateFormCatalogs.selectedPersonaStateText(
          personaID: draft.personaID,
          personas: activePersonas
        )
      )
      .scaledFont(.caption)
      .foregroundStyle(HarnessMonitorTheme.secondaryInk)
      .fixedSize(horizontal: false, vertical: true)
    }
  }

  private var acpFallbackRoleMenu: some View {
    SessionWindowCreateFieldBlock(title: "If a leader already runs in this session") {
      Picker("Fallback role", selection: selectedFallbackRole) {
        ForEach(SessionRole.allCases.filter { $0 != .leader }, id: \.self) { role in
          Text(role.title).tag(role)
        }
      }
      .pickerStyle(.menu)
      .harnessNativeFormControl()
      .accessibilityLabel("Fallback role")
    }
  }

  @ViewBuilder
  private func terminalTransportNotice(
    option: AgentCapabilityOption,
    choice: AgentCapabilityTransportChoice
  ) -> some View {
    if let unavailableReason = SessionWindowCreateFormCatalogs.unavailableReason(
      for: option,
      choice: choice
    ),
      !option.isEnabled(choice)
    {
      Label {
        Text(unavailableReason)
          .scaledFont(.caption)
          .fixedSize(horizontal: false, vertical: true)
      } icon: {
        Image(systemName: "exclamationmark.triangle.fill")
      }
      .foregroundStyle(HarnessMonitorTheme.caution)
      .accessibilityElement(children: .combine)
    }

    if option.showsInstallCTA {
      HarnessMonitorActionButton(
        title: option.installActionTitle,
        tint: HarnessMonitorTheme.caution,
        variant: .prominent,
        accessibilityIdentifier: HarnessMonitorAccessibility.agentCapabilityInstallButton(option.id),
        fillsWidth: false
      ) {
        HarnessMonitorClipboard.copy(SessionWindowCreateFormCatalogs.installHintText(for: option))
      }
      .help(SessionWindowCreateFormCatalogs.installHintText(for: option))
      .accessibilityHint(option.installAccessibilityHint ?? "")
    }

    if option.availabilityState == .checkingAccess
      || option.availabilityState == .setupRequired
      || option.availabilityState == .bridgeAccessRequired
      || option.availabilityState == .unavailable
    {
      SessionWindowCreateDiagnosticsDisclosure(option: option)
    }
  }

  private var codexConfigurationFields: some View {
    DisclosureGroup("Configure") {
      VStack(alignment: .leading, spacing: HarnessMonitorTheme.sectionSpacing) {
        SessionWindowCreateFieldBlock(title: "Run mode") {
          Picker("Run mode", selection: codexMode) {
            ForEach(CodexRunMode.allCases) { mode in
              Text(mode.title).tag(mode)
            }
          }
          .pickerStyle(.segmented)
          .harnessNativeFormControl()
          .accessibilityLabel("Codex mode")
        }

        if let codexCatalog {
          SessionWindowCreateFieldBlock(title: "Model") {
            Picker(
              selectedCodexModelMenuTitle(catalog: codexCatalog),
              selection: codexModelPickerSelection
            ) {
              ForEach(codexCatalog.models) { model in
                Text(model.displayName).tag(model.id)
              }
              Text("Custom...")
                .tag(SessionWindowCreateFormCatalogs.RuntimeCustomModel.tag)
            }
            .pickerStyle(.menu)
            .harnessNativeFormControl()
            .accessibilityLabel("Codex model")

            if codexModelPickerSelection.wrappedValue
              == SessionWindowCreateFormCatalogs.RuntimeCustomModel.tag
            {
              TextField("Provider-specific model id", text: codexCustomModel)
                .harnessNativeTextField()
                .accessibilityLabel("Custom Codex model")
            }
          }

          if !codexEffortValues.isEmpty {
            SessionWindowCreateFieldBlock(title: "Effort") {
              Picker("Effort", selection: codexEffortSelection) {
                ForEach(codexEffortValues, id: \.self) { level in
                  Text(level.capitalized).tag(level)
                }
              }
              .pickerStyle(.segmented)
              .harnessNativeFormControl()
              .accessibilityLabel("Codex effort")
            }
          }
        } else {
          SessionWindowCreateFieldBlock(title: "Model") {
            TextField("Model (optional)", text: codexCustomModel)
              .harnessNativeTextField()
              .accessibilityLabel("Codex model")
          }

          SessionWindowCreateFieldBlock(title: "Effort") {
            TextField("Effort (optional)", text: codexEffortText)
              .harnessNativeTextField()
              .accessibilityLabel("Codex effort")
          }
        }
      }
      .padding(.top, HarnessMonitorTheme.spacingSM)
    }
    .accessibilityLabel("Configure")
  }

  private var codexEffortText: Binding<String> {
    Binding(
      get: { draft.codexEffort },
      set: { updateDraft(codexEffort: $0) }
    )
  }

  private func selectedTerminalModelMenuTitle(
    runtime: AgentTuiRuntime,
    catalog: RuntimeModelCatalog
  ) -> String {
    let selectedModelID = terminalModelPickerSelection(for: runtime, catalog: catalog).wrappedValue
    if selectedModelID == SessionWindowCreateFormCatalogs.RuntimeCustomModel.tag {
      let customModelID = terminalCustomModel(for: runtime).wrappedValue
        .trimmingCharacters(in: .whitespacesAndNewlines)
      return customModelID.isEmpty ? "Custom model" : customModelID
    }

    return
      catalog.models.first { $0.id == selectedModelID }?.displayName
      ?? selectedModelID
  }

  private func selectedCodexModelMenuTitle(catalog: RuntimeModelCatalog) -> String {
    let selectedModelID = codexModelPickerSelection.wrappedValue
    if selectedModelID == SessionWindowCreateFormCatalogs.RuntimeCustomModel.tag {
      let customModelID = codexCustomModel.wrappedValue.trimmingCharacters(
        in: .whitespacesAndNewlines
      )
      return customModelID.isEmpty ? "Custom model" : customModelID
    }

    return
      catalog.models.first { $0.id == selectedModelID }?.displayName
      ?? selectedModelID
  }

  private func terminalModelPickerSelection(
    for runtime: AgentTuiRuntime,
    catalog: RuntimeModelCatalog
  ) -> Binding<String> {
    Binding(
      get: {
        if let stored = draft.modelByRuntime[runtime.rawValue], !stored.isEmpty {
          return stored
        }
        return catalog.default
      },
      set: { newValue in
        var next = draft
        next.modelByRuntime[runtime.rawValue] = newValue
        if newValue != SessionWindowCreateFormCatalogs.RuntimeCustomModel.tag {
          next.customModelByRuntime[runtime.rawValue] = nil
        }
        state.updateCreateDraft(next)
      }
    )
  }

  private func terminalCustomModel(
    for runtime: AgentTuiRuntime
  ) -> Binding<String> {
    Binding(
      get: { draft.customModelByRuntime[runtime.rawValue] ?? "" },
      set: {
        var next = draft
        next.modelByRuntime[runtime.rawValue] = SessionWindowCreateFormCatalogs.RuntimeCustomModel.tag
        next.customModelByRuntime[runtime.rawValue] = $0
        state.updateCreateDraft(next)
      }
    )
  }

  private func terminalEffortValues(
    for runtime: AgentTuiRuntime,
    catalog: RuntimeModelCatalog
  ) -> [String] {
    SessionWindowCreateFormCatalogs.effortValues(
      catalog: catalog,
      selectedModelID: terminalModelPickerSelection(for: runtime, catalog: catalog).wrappedValue
    )
  }

  private func terminalEffortSelection(
    for runtime: AgentTuiRuntime,
    values: [String]
  ) -> Binding<String> {
    Binding(
      get: {
        guard let current = draft.effortByRuntime[runtime.rawValue], values.contains(current) else {
          return SessionWindowCreateFormCatalogs.defaultEffortLevel(from: values)
        }
        return current
      },
      set: {
        var next = draft
        next.effortByRuntime[runtime.rawValue] = $0
        state.updateCreateDraft(next)
      }
    )
  }
}
